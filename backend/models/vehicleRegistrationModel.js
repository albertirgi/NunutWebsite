export default class VehicleRegistration{
  constructor(id, aggrLetter, color, expiredAt, license, licensePlate, name, note, phone, studentIdCard, type, userId){
    this.id = id;
    this.aggrLetter = aggrLetter;
    this.color = color;
    this.expiredAt = expiredAt;
    this.license = license;
    this.licensePlate = licensePlate;
    this.name = name;
    this.note = note;
    this.phone = phone;
    this.studentIdCard = studentIdCard;
    this.type = type;
    this.userId = userId;
  }
}
